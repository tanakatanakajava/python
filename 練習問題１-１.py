a=5
b=2

wa=a+b
sa=a-b
seki=a*b
shou=a//b
amari=a/b
# a//bは、商と余りを表示
# a/bは、商を表示（余りを表示しない）

print(a,"+",b,'=',wa)
print(a,"-",b,'=',sa)
print(a,'*',b,'=',seki)
print(a,"//",b,'=',shou)
print(a,"/",b,'=',amari)

#実行結果
#5 + 2 = 7
#5 - 2 = 3
#5 * 2 = 10
#5 // 2 = 2
#5 / 2 = 2.5

     
